/*
 Looping statements(LS):
   LS are used to execute any statement/s repeatedly
   
   Types of LS:
   1. For loop
   2. While loop
   3. Do-while loop
   
  For loop:
  	Syntax: for(Initialization; Condition; Increment/decrement)
 */
package basics;

public class LoopingStatements {

	public static void main(String[] args) {
		
		/*
		for(int count = 1; count<=5; count++) {
			System.out.println("Hello, world!");
			
		}
		*/
		
		/*
		for(int count = 5; count>=1; count--) {
			System.out.println("Hello, world!");
		}
		*/
		
		/*
		int count = 6;
		while(count<=5) {
			System.out.println("Hello, world!");
			count++;
		}
		*/
		/*
		int count = 6;
		do {
			System.out.println("Hello, world!");
			count++;
		}while(count<=5);
		*/
		/*
		int count = 6;
		while(count>=5) {
			System.out.println("Hello, world!");
			count++;
		}
		*/
		
		
		/* Infinite loop */
		/*
		while (true) {
			System.out.println("Hello, world!");
		}
		*/

	}

}
